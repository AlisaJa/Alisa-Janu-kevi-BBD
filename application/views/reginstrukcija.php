          <div class="indent-left3">
            <h3>Registracijos pateikimas</h3><br></br>
              <div class="wrapper border-bot2 prev-indent-bot"> <strong class="numb">1.</strong>
                <div class="extra-wrap"> <span class="color-5">Norėdami užsiregistruoti, paspauskite ant „Registracijos“ mygtuko meniu juostoje. Atsidarys naujas langas su visomis mūsų išvardintomis paslaugomis. Paspauskite ant norimos paslaugos ir būsite nukreipti į kitą žingsnį.</div>
              </div>
              <div class="wrapper border-bot2 prev-indent-bot"> <strong class="numb">2.</strong>
                <div class="extra-wrap"> <span class="color-5">Kitame žingsnyje turite išsirinkti norimą laisvą laiką ir/arba specialistą. Pasirinkę tam tikrą specialistą - Jums rodys tik to specialisto galimas dienas. Paspaudę ant norimos dienos kalendoriuje - šalimais atsivers laikų pasirinkimas. Pasirinkę galimą laisvą laiką, pasirodys mygtukas „Tęsti“. Spauskite jį norėdami pereiti į kitą žingsnį. </div>
              </div>
              <div class="wrapper p2"> <strong class="numb">3.</strong>
                <div class="extra-wrap"><span class="color-5">Perėjus į kitą langą (kuomet esate išsirinkę paslaugą ir norimą laisvą laiką), turite pateikti visus reikiamus asmeninius duomenis (vardas, pavardė, el. paštas ir telefono numeris). Šalimais matysite savo vizito informaciją: pasirinktą paslaugą, vizito datą bei laiką, pasirinktą specialistą (jeigu tokį pasirinkote) bei bendrą užsakymo sumą.</div>
              </div>
              <div class="wrapper p2"> <strong class="numb">4.</strong>
                <div class="extra-wrap"><span class="color-5">Paskutiniame lange galite pritaikyti nuolaidą bendrai užsakymo sumai. Norėdami pritaikyti nuolaidą, įveskite nuolaidos kodą į tam skirtą vietą ir spauskite mygtuką „Pritaikyti“. Jeigu kodą suvedėte teisingai - pasikeis užsakymo suma ir ekrane matysite sėkmės pranešimą. Jeigu kodą įvedėte neteisingai - ekrane matysite klaidos pranešimą.</div>
              </div>
              <div class="wrapper p2"> <strong class="numb">5.</strong>
                <div class="extra-wrap"><span class="color-5">Užpildę visus reikiamus laukus, spauskite mygtuką „Registruotis“. Sėkmingos registracijos atveju sistema parodys pranešimą „Sveikiname, Jūsų registracija sėkminga“. Tuomet pasitikrinkite savo el.paštą (kurį įvedėte registruojantis). El.pašte turėtumėte rasti laišką su Jūsų vizito informacija bei atšaukimo nuoroda.</div>
              </div>
